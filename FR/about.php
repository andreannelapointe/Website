<h3>À propos</h3>

<p>Les étudiants de recherche de premier cycle universitaire représentent une grande
importance envers la recherche qui est parfois non reconnu. Pour avancer les études
universitaires, les publications et les conférences deviennent très prioritaires et
les opportunités pour les étudiants de discuter leur recherche sur l’échelle nationale
est parfois non-apparente. Le CCÉM a commencé en 1994 pour but de remplir ce besoin.
Avec ses origines de l’Université McGill, le CCÉM est maintenant l’une des plus grandes
conférences pour étudiants de premier cycle universitaire en Amérique du Nord.</p>

<p>La recherche en mathématique n’est pas limitée à la recherche en mathématiques pures;
les mathématiques sont utilisés dans beaucoup de domaines. Les méthodes de statistiques
sont appliquées dans presque tous les domaines de recherche et d’autres méthodes de
mathématiques sont utilisées dans des domaines diverses tel que la physique, l’économie,
la bioinformatique, les statistiques, l’informatique, la finance et l’actuariat. L’échange
des idées en mathématiques entre divers domaines a révolutionné la compréhension dans notre
monde.</p>

<p>Cette conférence a pour but de donner aux étudiants universitaires de premier cycle, dans
tous les domaines de recherche, une chance d’en apprendre plus sur les mathématiques et sur
la recherche mathématique au-delà de ce qui est offert dans les classes universitaires. Les
étudiants n’ont pas seulement la chance de discuter avec les autres étudiants d’à travers le
pays, qui partagent des intérêts dans le même domaine, mais ce congrès invite aussi des étudiants
internationales, venant des États-Unis, de l’Europe et de l’Asie du Sud.</p>

<p>À travers la conférence, les étudiants auront l’opportunité de faire une présentation sur un
sujet de mathématique de leur choix, ce qui leur donnera de l’expérience en présentations et une
chance de recevoir des réactions et des propos de leurs collègues. Les étudiants découvriront des
nouveaux sujets de mathématique et ils écouteront à des orateurs venant de disciplines variées. Ces
orateurs, invités au CCÉM, sont des chercheurs établis ou des chercheurs grandissant dans leur domaine,
et ils mettent beaucoup d’importance envers les étudiants en mathématiques. Toutes étudiants participant
partiront avec des nouvelles expériences, de la nouvelle comprehension, des nouvelles idées et avec
espoir, une nouvelle passion pour les mathématiques.</p>
