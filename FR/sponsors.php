<h2>Commanditaires</h2>

<p>
  Le CCÉM est un événement à but non-lucratif, organisé par des
  bénévoles dans le but de promouvoir et faure découvrir la recherche
  en mathématiques des étudiants de premier cycle. Le CCÉM obtient son
  financement en partie à partir de frais d’inscriptions mais aussi
  grâce aux contributions de plusieurs commanditaires.
</p>
