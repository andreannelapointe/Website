<h2>Conférenciers</h2>

<p>
  Huit conférences plénières se dérouleront dans le cadre du CCÉM,
  couvrant des domaines des mathématiques aussi variés que
  possible. Ces conférences, données par des professeurs ou des
  chercheurs provenant de l’industrie et reconnus dans leur domaine,
  permettront aux participants au CCÉM de s’ouvrir sur les sujets qui
  leurs seront présenter et d’élargir leurs horizons.
</p>

<p>
  Nous ferons appel non seulement à des conférenciers reconnus pour
  leur apport scientifique, mais aussi de bons vulgarisateurs, qui
  sauront transmettre aux auditeurs leur passion pour les
  mathématiques. En choisissant des conférenciers provenant de
  domaines les plus variés possible, non seulement chacun y trouvera
  son compte, mais tous pourront aussi assister à des conférences sur
  des sujets qu'ils ne connaissaient pas à priori.
</p>
