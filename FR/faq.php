<div class="box">
  <h3>Questions des commanditaires</h3>

  <div class="question">
    Q: Quand se déroule l'événement?
  </div>

  <div class="answer">
    R: Le CCÉM se déroule du 10 au 14 juillet 2013, à l'Université de
    Montréal. Le CCÉM rassemblera des étudiants de premier cycle en
    mathématiques de partout au Canada.
  </div>

  <div class="question">
    Q: Qui organise le CCÉM?
  </div>

  <div class="answer">
    R: Le CCÉM est organisé par un comité d'étudiants en mathématiques
    et en physique mathématique de l'Université de Montréal, sous la
    tutelle de STUDC, le comité étudiant de la société mathématique du
    Canada. Le comité organisateur reçoit de l'aide du Département de
    Mathématiques et de Statistiques de l'Université de Montréal,
    ainsi que du Centre de Recherche en Mathématiques.
  </div>

  <div class="question">
    Q: Pourquoi devrait-on vous donner du financement?
  </div>

  <div class="answer">
    R: Le CCÉM rassemble environ 200 des meilleurs étudiants de
    premier cycle en mathématiques au pays. Augmenter la visibilité de
    votre centre de recherche, de votre département ou de votre
    entreprise auprès d'eux vous permettrait d'attirer des candidats
    intéressants, que ce soit pour des études graduées ou sur le
    marché du travail.
  </div>
